function getGtoAction(stackDepth, numPlayers, position, scenario, handKey) {
    try {
        const rangeData = GTO_RANGES[stackDepth]?.[`${numPlayers}max`]?.[position]?.[scenario];
        if (rangeData && rangeData[handKey]) {
            return rangeData[handKey];
        }
        // Finner vi ikke noe for denne spesifikke hånden, er default Fold
        return { "F": 1.0 };
    } catch (e) {
        console.error("Error fetching GTO action:", e, stackDepth, numPlayers, position, scenario, handKey);
        return { "F": 1.0 }; // Returner Fold ved feil
    }
}

function getFullRange(stackDepth, numPlayers, position, scenario) {
     try {
         // Få den definerte range-dataen for situasjonen, eller et tomt objekt hvis ikke funnet
        const baseRangeData = GTO_RANGES[stackDepth]?.[`${numPlayers}max`]?.[position]?.[scenario] || {};
        const completeRange = {};
        const ranksRev = ['A', 'K', 'Q', 'J', 'T', '9', '8', '7', '6', '5', '4', '3', '2'];

         // Bygg et komplett grid med 169 hender
         ranksRev.forEach((r1, i) => {
             ranksRev.forEach((r2, j) => {
                 let key;
                 if (i === j) key = r1 + r2; // Pair (f.eks. AA)
                 else if (i < j) key = r1 + r2 + 's'; // Suited (f.eks. AKs)
                 else key = r2 + r1 + 'o'; // Offsuit (f.eks. KQo)

                 // Bruk dataen fra baseRangeData hvis den finnes for denne nøkkelen, ellers default til Fold
                 completeRange[key] = baseRangeData[key] || { "F": 1.0 };
             });
         });
        return completeRange; // Returner det komplette 169-hånds objektet
    } catch (e) {
        console.error("Error fetching full range:", e, stackDepth, numPlayers, position, scenario);
        return {}; // Returner tomt objekt ved feil
    }
}
